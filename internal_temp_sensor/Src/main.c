/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC3_Init();
  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */

  /* Get temperature calibration values */
  const uint32_t *TS_CAL1 = (const uint32_t*) 0x1FF1E820;
  const uint32_t *TS_CAL2 = (const uint32_t*) 0x1FF1E840;

  /* Run ADC calibration in single-ended mode */
  HAL_ADCEx_Calibration_Start(&hadc3, ADC_CALIB_OFFSET, ADC_SINGLE_ENDED);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  uint32_t adc_value;
	  double temp_in_C;
	  char msg[5];
	  char *to_print = "Temperature is: ";

	  /* Start the ADC Conversion */
	  HAL_ADC_Start(&hadc3);

	  /* Wait for conversion to be done */
	  while (HAL_ADC_PollForConversion(&hadc3, 10) != HAL_OK);

	  /* Convert value to Celsius */
	  adc_value = HAL_ADC_GetValue(&hadc3);
	  temp_in_C = (double)(110-30)/(*TS_CAL2 - *TS_CAL1)*((int32_t)adc_value - (int32_t)*TS_CAL1) + 30;

	  /* Stop the Conversion */
	  HAL_ADC_Stop(&hadc3);

	  /* Print */
	  double_to_string(temp_in_C, msg);

	  HAL_UART_Transmit(&huart3, (uint8_t *)to_print, strlen(to_print), 0xFF);
	  HAL_UART_Transmit(&huart3, (uint8_t *)msg, 5, 0xFF);

	  HAL_Delay(2000);
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

